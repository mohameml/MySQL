-- COUNT :

SELECT COUNT(*)  AS Total FROM fv_games ;

-- AVG :
SELECT AVG(game_price)  AS Moyenne FROM fv_games ;

-- MAX :
SELECT MAX(game_price)  AS Max FROM fv_games ;

-- MIN :
SELECT MIN(game_price)  AS Max FROM fv_games ;

-- SUM :
SELECT SUM(game_stock)  AS Total_game FROM fv_games ;

